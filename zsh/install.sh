#!/usr/bin/env bash
# install.sh — point zsh at this repo by writing ~/.zshenv
#
# No symlinks. The repo IS the config: ~/.zshenv just sets ZDOTDIR
# to the repo's zsh/ directory, and zsh reads everything from there.
#
# Usage:
#   ./install.sh          # base install
#   ./install.sh --work   # also enable work modules (az, k8s, Astra, SSH agent)
#   ./install.sh --uninstall
#
# Safe to re-run; existing files are backed up with a timestamp.
set -euo pipefail

REPO="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
ZDOTDIR="$REPO/zsh"
TS="$(date +%Y%m%d%H%M%S)"

WORK=0
UNINSTALL=0
for arg in "$@"; do
  case "$arg" in
    --work) WORK=1 ;;
    --uninstall) UNINSTALL=1 ;;
    -h|--help)
      sed -n '2,12p' "$0"
      exit 0
      ;;
    *)
      printf 'Unknown argument: %s\n' "$arg" >&2
      exit 1
      ;;
  esac
done

green()  { printf '\033[32m✓\033[0m %s\n' "$1"; }
yellow() { printf '\033[33m!\033[0m %s\n' "$1"; }
bold()   { printf '\033[1m%s\033[0m\n' "$1"; }

write_zshenv() {
  if [[ -e "$HOME/.zshenv" && ! -L "$HOME/.zshenv" ]]; then
    mv "$HOME/.zshenv" "$HOME/.zshenv.bak.$TS"
    yellow "Backed up ~/.zshenv → ~/.zshenv.bak.$TS"
  fi

  {
    printf '# Generated by zsh/install.sh on %s\n' "$(date -Iseconds)"
    printf '# Points zsh at this repo. Edit the path if you move the repo.\n'
    printf 'export ZDOTDIR=%q\n' "$ZDOTDIR"
    # zsh reads EITHER ~/.zshenv OR $ZDOTDIR/.zshenv depending on whether
    # ZDOTDIR is set at startup. Source $ZDOTDIR/.zshenv here so the XDG vars,
    # starship path, and work env are always loaded — even when this file
    # itself is being read as ~/.zshenv.
    printf '[ -f "$ZDOTDIR/.zshenv" ] && source "$ZDOTDIR/.zshenv"\n'
    if [[ "$WORK" -eq 1 ]]; then
      printf '\n# Work modules enabled (Astra/Kubernetes/CA/SSH-agent).\n'
      printf 'export ZSH_WORK=1\n'
    fi
  } > "$HOME/.zshenv"

  local work_suffix=""
  [[ "$WORK" -eq 1 ]] && work_suffix=" (ZSH_WORK=1)"
  green "Wrote ~/.zshenv → ZDOTDIR=$ZDOTDIR$work_suffix"
}

cleanup_legacy_symlinks() {
  # Previous install versions created symlinks under ~/.zsh/. They're no
  # longer needed (zsh reads directly from the repo via ZDOTDIR).
  if [[ -L "$HOME/.zsh" ]]; then
    rm "$HOME/.zsh"
    green "Removed legacy symlink ~/.zsh"
  fi
  if [[ -L "$HOME/.zshenv" ]]; then
    rm "$HOME/.zshenv"
    green "Removed legacy symlink ~/.zshenv"
  fi
}

if [[ "$UNINSTALL" -eq 1 ]]; then
  bold "==> Uninstalling"
  [[ -e "$HOME/.zshenv" ]] && mv "$HOME/.zshenv" "$HOME/.zshenv.uninstalled.$TS" && yellow "Moved ~/.zshenv → ~/.zshenv.uninstalled.$TS"
  cleanup_legacy_symlinks
  bold "==> Done"
  exit 0
fi

bold "==> Installing"
cleanup_legacy_symlinks
write_zshenv

echo ""
bold "==> Done"
echo "  Open a new terminal or run:  exec zsh -l"
echo ""
echo "  First launch auto-installs missing plugins:"
echo "    zsh-autosuggestions"
echo "    zsh-history-substring-search"
echo "    fast-syntax-highlighting"
echo ""
echo "  To toggle work modules later, edit ~/.zshenv (add/remove 'export ZSH_WORK=1')"
echo "  or re-run:  ./install.sh --work"
echo ""
echo "  Recommended tools to install:"
echo "    eza bat fd-find ripgrep fzf zoxide starship mise direnv keychain nvm lf nvim"

if [[ "$WORK" -eq 1 ]]; then
  echo ""
  bold "==> Action required: Azure IDs"
  echo "  Copy the template and fill in the IDs:"
  echo "      cp $ZDOTDIR/work/az.env.example $ZDOTDIR/work/az.env"
  echo "      \$EDITOR $ZDOTDIR/work/az.env"
  echo ""
  echo "  Then replace the placeholder values:"
  echo ""
  echo "      export AZ_TENANT_ID=\"<your-tenant-guid>\""
  echo "      export AZ_SUBSCRIPTION_ID=\"<your-subscription-guid>\""
  echo ""
  echo "  Find them with:"
  echo "      az account tenant list   --query '[].tenantId' -o tsv"
  echo "      az account list          --query '[].{name:name,id:id}' -o table"
  echo ""
  echo "  az.env is git-ignored so your real IDs stay local to this machine."
fi
